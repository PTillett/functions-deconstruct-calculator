const subtract = (num1, num2) => {
    const difference = num1 - num2;
    return difference;
}
const divide = (num1, num2) => {
    const rst = num1 / num2;
    return rst;
}
const multiply = (num1, num2) => {
    const product = num1 * num2;
    return product;
}
const square = (num1) => {
    const sqr = num1 * num1
    return sqr;
}
